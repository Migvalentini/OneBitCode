/*
HTML EVENTS:

=> They are a dynamic way to execute javascript inside an html page
=> They are activated by specific actions in the page (click, hover, type, etc)
*/

function mouseEnterEvent() {
   console.log('mouse entered')
 }
 
 function changeEvent() {
   console.log('value changed')
 }
 
 function focusEvent() {
   console.log('gained focus')
 }
 
 function blurEvent() {
   console.log('lost focus')
 }
 
 function clickEvent() {
   console.log('clicked')
 }