/*
TYPESCRIPT VS JAVASCRIPT:

=> The same syntax:
   - Declaration of Variables
   - Logical and Arithmetic Operators
   - Conditional and Loops
   - Functions
   - Objects
   - Promises
   - etc
=> Support to ES6+:
   - Classes
   - Arrow Functions
   - Async and Await
   - etc
=> Typed Variables:
   - Js: Weak and Dynamic Typing
   - Ts: Strong and Static Typing
=> Working in Browser:
   - Js: Runs on Any Browser
   - Ts: Don't run in any browser, must be transformed into Js code
=> Working in Node.js:
   - Js: Standard language supported by Node
   - Ts: Also ins't supported by Node and must be transformed into Js code
*/